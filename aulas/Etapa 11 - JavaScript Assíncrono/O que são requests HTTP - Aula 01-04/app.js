/*
    API - Application Programming Interface
        É uma ferramenta que permite a comunicação entre uma aplicação e outra, permitindo também que uma aplicação possa utilizar recursos dessa outra aplicação.

    Para ocorrer essa comunicação, precisamos fazer uma requisição HTTP (Hypertext Transfer Protocol)

    Essa requisição é feita para um ENDPOINT da API ou banco de dados escolhido. O endpoint contém uma URL que gera uma resposta em formato JSON (JavaScript Object Notation), um formato muito parecido com um objeto JavaScript.
*/